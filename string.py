#文字列(Strings)

print("Hello world!!")
print('Hello world!!')

#クォーテーションの中に使いたい場合は別の種類でやる
print("I'm fine")
print('I"m fine')

#クォーテーション3つの場合は改行してもいける
print("""
Hello world!!
How are you doing?
""")

#\nで改行、\tでタブ入力ができる
print('hello  \nworld')
print('hello \tworld')
#エスケープシーケンス
print('back slash n:\\n ')
print('I\'m fine')

print('hello' + 'world' + '!!')
